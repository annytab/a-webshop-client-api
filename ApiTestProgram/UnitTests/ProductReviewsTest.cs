        [TestMethod]
        public async Task TestAdd()
        {
            // Create the connection
            ClientConnection connection = new ClientConnection("https://localhost:44301", "TestAPI", "test");

            // Create a new post
            ProductReview productReview = new ProductReview();
            productReview.id = 0;
            productReview.product_id = 4;
            productReview.customer_id = 2;
            productReview.language_id = 1;
            productReview.review_date = DateTime.MinValue;
            productReview.review_text = "Review text dfad";
            productReview.rating = 2.77M;

            // Add the post
            ResponseMessage response = await ProductReview.Add(connection, productReview);

            // Dispose of the connection
            connection.Dispose();

            // Test the method call
            Assert.AreEqual(true, response.is_success, response.status_code + " - " + response.reason_phrase + " - " + response.message);

        } // End of the TestAdd method

